//char and boolean in java
public class charAndBoolean {
    public static void main(String[] args) {
        char myChar = 'D'; // single quotes
        System.out.println(myChar); // D

        // in unicode : for unicodes visit www.unicode-table.com
        char myUnicode = '\u0044';
        System.out.println(myUnicode); // D

        // in Decimal code
        char myDecimalCode = 68;
        System.out.println(myDecimalCode);

        System.out.println('D' == 68); // true

        // boolean
        boolean myTrueBooleanValue = true;
        boolean myFalseBooleanValue = false;

    }
}
